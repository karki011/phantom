/**
 * Persist a full xterm visual state snapshot for `paneId`. Generated by
 * `@xterm/addon-serialize`. Replayed on next restore via `terminal.write(state)`.
 */
export async function saveTerminalSnapshot(paneId: string, state: string): Promise<void> {
  try {
    await App()?.SaveTerminalSnapshot(paneId, state);
  } catch (error) {
    console.error('[terminal] saveTerminalSnapshot failed', { paneId, error });
  }
}

/**
 * Fetch the previously-saved xterm state for `paneId`. Returns empty string
 * if no snapshot exists. The caller writes the result into the xterm
 * instance with `terminal.write(state)` BEFORE attaching to the live PTY.
 */
export async function getTerminalSnapshot(paneId: string): Promise<string> {
  try {
    return (await App()?.GetTerminalSnapshot(paneId)) ?? '';
  } catch {
    return '';
  }
}

