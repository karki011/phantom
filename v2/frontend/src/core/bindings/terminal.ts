const App = () => (window as any).go?.['app']?.App;

export async function createTerminal(
  id: string,
  worktreeId: string,
  projectId: string,
  cwd: string,
  cols: number,
  rows: number,
): Promise<void> {
  try {
    await App()?.CreateTerminal(id, worktreeId, projectId, cwd, cols, rows);
  } catch (error) {
    console.error('[terminal] createTerminal failed', { id, error });
  }
}

